/*
for and while loops
for syntax
for (initialization; condition; update) {
  // code to be executed
}
initialization-executed before the loop starts
condition-statement that is evaluated at the beginning of each iteration of the loop
update-executed at the end of each iteration of the loop

while loops
while (condition) {
  // code to be executed
}

*/

//for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
  }

//while loop
i = 0;

while (i < 5) {
  console.log(i);
  i++;
}

  